/*
 * Write a program that displays the area and perimeter of a circle that has a radius of 5.5 using the following formula:
 * perimeter = 2 * radius * pi
 * area = radius * radius * pi
 */

public class Problem1_8 {
	public static void main(String[] args ){
		System.out.println("The circle has a radius of 5.5. What is the area and perimeter of the circle?");
		System.out.println("The perimeter is " + (2.0 * 5.5 * 3.14));
		System.out.println("The area is " + (5.5 * 5.5 * 3.14));
	}

}
