package ch1;
/*
 * Write a program that displays the area and perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following formula:
 * area = width * height
 */

public class Problem1_9 {
	public static void main(String[] args ){
		System.out.println("The height of a rectangle is 7.9 and the width is 4.5. What is the area and perimeter?");
		System.out.println("The area is " + (4.5 * 7.9));
		System.out.println("The perimeter is " + (4.5 + 4.5 + 7.9 + 7.9));
	}
}
